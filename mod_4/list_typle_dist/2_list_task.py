raw_ips = ["192.168.1.10", "  10.0.0.5 ", "192.168.1.10", "", "127.0.0.1", "10.0.0.1"]
clean_ips = []

for ip in raw_ips:
    cleaned = ip.strip()
    # Проверяем условия фильтрации
    if cleaned and cleaned != "127.0.0.1" and cleaned not in clean_ips:
        clean_ips.append(cleaned)

clean_ips.sort()  # Сортировка списка на месте
print(f"Очищенный инвентарь: {clean_ips}")

